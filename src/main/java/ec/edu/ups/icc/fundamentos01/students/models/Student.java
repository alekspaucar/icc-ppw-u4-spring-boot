package ec.edu.ups.icc.fundamentos01.students.models;


public class Student {

    private long id;
    private String name;
    private String age;

    public Student(long id, String name, String age) {
        this.id = id;
        this.name = name;
        this.age = age;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
        

}

